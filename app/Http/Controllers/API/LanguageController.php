    public function list()
    {
        return $this->successResponse([
            'languages' => $this->supportedLanguages,
            'default' => 'hi',
            'current' => App::getLocale(),
        ]);
    }

    public function setLanguage(Request $request)
    {
        $validator = \Validator::make($request->all(), [
            'language' => 'required|string|in:' . implode(',', array_keys($this->supportedLanguages)),
        ]);

        if ($validator->fails()) {
            return $this->errorResponse('Validation failed', 422, $validator->errors());
        }

        $language = $request->language;

        // Set locale for current request
        App::setLocale($language);

        // Save to user preferences if authenticated
        if ($request->user()) {
            $request->user()->update(['language' => $language]);
        }

        return $this->successResponse([
            'language' => $language,
            'name' => $this->supportedLanguages[$language],
        ], 'Language changed successfully');
    }

    public function getTranslations(Request $request)
    {
        $language = $request->get('language', App::getLocale());

        if (!array_key_exists($language, $this->supportedLanguages)) {
            $language = 'hi';
        }

        $translations = [];

        // Load translations from lang files
        $langPath = resource_path("lang/{$language}");
        if (is_dir($langPath)) {
            foreach (glob($langPath . '/*.php') as $file) {
                $key = basename($file, '.php');
                $translations[$key] = require $file;
            }
        }

        // Common business terms in all languages
        $businessTerms = $this->getBusinessTerms($language);

        return $this->successResponse([
            'language' => $language,
            'translations' => $translations,
            'business_terms' => $businessTerms,
        ]);
    }

    public function translate(Request $request)
    {
        $validator = \Validator::make($request->all(), [
            'text' => 'required|string',
            'target_language' => 'required|string|in:' . implode(',', array_keys($this->supportedLanguages)),
        ]);

        if ($validator->fails()) {
            return $this->errorResponse('Validation failed', 422, $validator->errors());
        }

        // For production, integrate with Google Translate API or similar
        // For now, return a placeholder response
        return $this->successResponse([
            'original' => $request->text,
            'translated' => $request->text, // Placeholder - integrate translation API
            'target_language' => $request->target_language,
        ], 'Translation service ready - integrate Google Translate API for production');
    }

